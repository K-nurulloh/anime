export const getLang = () => localStorage.getItem('lang') || 'uz';

export const setLang = (lang) => {
  localStorage.setItem('lang', lang);
};

export const t = (key) => {
  const lang = getLang();
  return translations[lang]?.[key] || translations.uz[key] || key;
};

export const applyTranslations = () => {
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    el.setAttribute('placeholder', t(el.dataset.i18nPlaceholder));
  });
  document.querySelectorAll('[data-i18n-value]').forEach((el) => {
    el.value = t(el.dataset.i18nValue);
  });
};

export const initLangSwitcher = () => {
  const switcher = document.querySelector('[data-lang-switch]');
  if (!switcher) return;
  switcher.value = getLang();
  switcher.addEventListener('change', () => {
    setLang(switcher.value);
    applyTranslations();
    window.dispatchEvent(new Event('langChanged'));
  });
};